			La generalización y adopción de los nuevos medios de alta definición, así como la complejidad de los trabajos audiovisuales e infografías hacen que el proceso de renderización se convierta en un elemento crucial en un proyecto 3D.
<br>
El uso de una granja de render permite reducir drásticamente los cálculos complejos de los proyectos. La Cloud es entonces la manera en que la informática se ajusta a los presupuestos de artistas y estudios ya que sólo se paga por lo que usa. Ya no hay que preocuparse más por realizar inversiones elevadas para tener una granja profesional ni de incurrir en costes de:
<br>
    - software (adquisición de licencias por nodo y actualizaciones)<br>
    - hardware (actualización y ampliación de los equipos)<br>
    - instalación de infraestructura adecuada<br>
    - mantenimiento de los equipos en un entorno refrigerado<br>
    - consumo eléctrico y otros gastos derivados<br>
    - gestión, instalación y administración por un personal competente<br>
